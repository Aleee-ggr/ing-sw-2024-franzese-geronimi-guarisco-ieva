package it.polimi.ingsw.model.enums;

/**
 * Represents the possible states of the game.
 * The game can be in the LOGIN state when players are logging in,
 * in the START state when the game is starting,
 * in the IN_GAME state during gameplay,
 * and in the END state when the game is over.
 * @author Gloria Geronimi
 */
public enum GameState {
    LOGIN, START, IN_GAME, END
}
